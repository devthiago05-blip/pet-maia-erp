create or replace function public.create_pos_quote(
  customer_tutor_id bigint,
  customer_name text,
  expiration_date date,
  items jsonb
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  new_quote_id bigint;
  item jsonb;
  quote_total numeric(12, 2) := 0;
  product_record public.products%rowtype;
  item_quantity integer;
begin
  if not public.current_user_can_access('pdv') then
    raise exception 'Acesso negado ao PDV';
  end if;

  if jsonb_array_length(items) = 0 then
    raise exception 'O orçamento precisa ter ao menos um item';
  end if;

  for item in select * from jsonb_array_elements(items)
  loop
    select * into product_record
    from public.products
    where id = (item ->> 'product_id')::bigint
      and ativo = true;

    if not found then
      raise exception 'Produto inválido';
    end if;

    item_quantity := (item ->> 'quantidade')::integer;
    if item_quantity <= 0 then
      raise exception 'Quantidade inválida';
    end if;

    quote_total := quote_total + (product_record.preco_venda * item_quantity);
  end loop;

  insert into public.pos_quotes (
    tutor_id,
    cliente_nome,
    total,
    validade
  )
  values (
    customer_tutor_id,
    nullif(trim(customer_name), ''),
    quote_total,
    expiration_date
  )
  returning id into new_quote_id;

  for item in select * from jsonb_array_elements(items)
  loop
    select * into product_record
    from public.products
    where id = (item ->> 'product_id')::bigint;

    item_quantity := (item ->> 'quantidade')::integer;

    insert into public.pos_quote_items (
      quote_id,
      product_id,
      descricao,
      quantidade,
      valor_unitario,
      subtotal
    )
    values (
      new_quote_id,
      product_record.id,
      product_record.nome,
      item_quantity,
      product_record.preco_venda,
      product_record.preco_venda * item_quantity
    );
  end loop;

  return new_quote_id;
end;
$$;

create or replace function public.create_pos_sale(
  customer_tutor_id bigint,
  customer_name text,
  payment_method text,
  items jsonb
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  new_sale_id bigint;
  item jsonb;
  sale_total numeric(12, 2) := 0;
  product_record public.products%rowtype;
  item_quantity integer;
begin
  if not public.current_user_can_access('pdv') then
    raise exception 'Acesso negado ao PDV';
  end if;

  if jsonb_array_length(items) = 0 then
    raise exception 'A venda precisa ter ao menos um item';
  end if;

  for item in select * from jsonb_array_elements(items)
  loop
    select * into product_record
    from public.products
    where id = (item ->> 'product_id')::bigint
      and ativo = true
    for update;

    if not found then
      raise exception 'Produto inválido';
    end if;

    item_quantity := (item ->> 'quantidade')::integer;
    if item_quantity <= 0 or product_record.estoque < item_quantity then
      raise exception 'Estoque insuficiente para %', product_record.nome;
    end if;

    sale_total := sale_total + (product_record.preco_venda * item_quantity);
  end loop;

  insert into public.pos_sales (
    tutor_id,
    cliente_nome,
    total,
    forma_pagamento
  )
  values (
    customer_tutor_id,
    nullif(trim(customer_name), ''),
    sale_total,
    payment_method
  )
  returning id into new_sale_id;

  for item in select * from jsonb_array_elements(items)
  loop
    select * into product_record
    from public.products
    where id = (item ->> 'product_id')::bigint
    for update;

    item_quantity := (item ->> 'quantidade')::integer;

    insert into public.pos_sale_items (
      sale_id,
      product_id,
      descricao,
      quantidade,
      valor_unitario,
      subtotal
    )
    values (
      new_sale_id,
      product_record.id,
      product_record.nome,
      item_quantity,
      product_record.preco_venda,
      product_record.preco_venda * item_quantity
    );

    update public.products
    set
      estoque = estoque - item_quantity,
      updated_at = now()
    where id = product_record.id;
  end loop;

  insert into public.financial_entries (
    descricao,
    valor,
    tipo,
    forma_pagamento,
    status_pagamento
  )
  values (
    format('Venda PDV #%s - %s', new_sale_id, coalesce(nullif(trim(customer_name), ''), 'Consumidor')),
    sale_total,
    'Receita',
    payment_method,
    'Pago'
  );

  return new_sale_id;
end;
$$;

revoke all on function public.create_pos_quote(bigint, text, date, jsonb) from public;
revoke all on function public.create_pos_sale(bigint, text, text, jsonb) from public;
grant execute on function public.create_pos_quote(bigint, text, date, jsonb) to authenticated;
grant execute on function public.create_pos_sale(bigint, text, text, jsonb) to authenticated;

