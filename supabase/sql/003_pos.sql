alter table public.user_permissions
add constraint user_permissions_module_check check (
  module in (
    'dashboard',
    'tutores',
    'pets',
    'servicos',
    'agenda',
    'financeiro',
    'recibos',
    'relatorios',
    'pdv',
    'configuracoes',
    'usuarios'
  )
);

insert into public.user_permissions (user_id, module, can_access)
select id, 'pdv', is_admin
from public.user_profiles
on conflict (user_id, module) do update
set can_access = excluded.can_access
where public.user_permissions.can_access = false;

create or replace function public.create_default_user_permissions(target_user_id uuid)
returns void
language sql
security definer
set search_path = public
as $$
  insert into public.user_permissions (user_id, module, can_access)
  select
    target_user_id,
    module,
    false
  from unnest(
    array[
      'dashboard',
      'tutores',
      'pets',
      'servicos',
      'agenda',
      'financeiro',
      'recibos',
      'relatorios',
      'pdv',
      'configuracoes',
      'usuarios'
    ]
  ) as modules(module)
  on conflict (user_id, module) do nothing;
$$;

