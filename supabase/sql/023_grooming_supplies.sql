begin;

create table if not exists public.grooming_supplies (
  id bigint generated by default as identity primary key,
  name text not null,
  category text not null default 'Geral',
  unit text not null default 'unidade',
  current_stock numeric(12, 3) not null default 0 check (current_stock >= 0),
  minimum_stock numeric(12, 3) not null default 0 check (minimum_stock >= 0),
  supplier text,
  notes text,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.grooming_supply_movements (
  id bigint generated by default as identity primary key,
  supply_id bigint not null references public.grooming_supplies(id) on delete cascade,
  movement_type text not null check (
    movement_type in (
      'entrada',
      'saida',
      'descarte',
      'vencido',
      'perda',
      'ajuste_positivo',
      'ajuste_negativo'
    )
  ),
  quantity numeric(12, 3) not null check (quantity > 0),
  unit_cost numeric(10, 2) not null default 0 check (unit_cost >= 0),
  total_cost numeric(10, 2) generated always as (round((quantity * unit_cost)::numeric, 2)) stored,
  supplier text,
  movement_date date not null default current_date,
  expiration_date date,
  payment_status text not null default 'Pago' check (payment_status in ('Pago', 'Pendente')),
  payment_method text,
  due_date date,
  financial_entry_id bigint references public.financial_entries(id) on delete set null,
  notes text,
  created_at timestamptz not null default now()
);

create table if not exists public.groomer_daily_payments (
  id bigint generated by default as identity primary key,
  professional_name text not null,
  work_date date not null default current_date,
  payment_type text not null default 'diaria' check (
    payment_type in ('diaria', 'comissao', 'extra')
  ),
  amount numeric(10, 2) not null check (amount >= 0),
  payment_status text not null default 'Pendente' check (payment_status in ('Pago', 'Pendente')),
  payment_method text,
  due_date date,
  financial_entry_id bigint references public.financial_entries(id) on delete set null,
  notes text,
  created_at timestamptz not null default now()
);

