on public.groomer_daily_payments(work_date);

create index if not exists idx_groomer_daily_payments_due_date
on public.groomer_daily_payments(due_date);

create or replace function public.get_grooming_supply_stock_delta(
  selected_movement_type text,
  selected_quantity numeric
)
returns numeric
language sql
immutable
as $$
  select case
    when selected_movement_type in ('entrada', 'ajuste_positivo') then coalesce(selected_quantity, 0)
    when selected_movement_type in (
      'saida',
      'descarte',
      'vencido',
      'perda',
      'ajuste_negativo'
    ) then -coalesce(selected_quantity, 0)
    else 0
  end;
$$;

create or replace function public.sync_grooming_supply_stock()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  old_delta numeric;
  new_delta numeric;
begin
  if tg_op = 'INSERT' then
    new_delta := public.get_grooming_supply_stock_delta(
      new.movement_type,
      new.quantity
    );

    update public.grooming_supplies
    set
      current_stock = current_stock + new_delta,
      updated_at = now()
    where id = new.supply_id;

    return new;
  end if;

  if tg_op = 'UPDATE' then
    old_delta := public.get_grooming_supply_stock_delta(
      old.movement_type,
      old.quantity
    );

    new_delta := public.get_grooming_supply_stock_delta(
      new.movement_type,
      new.quantity
    );

    if old.supply_id = new.supply_id then
      update public.grooming_supplies
      set
        current_stock = current_stock - old_delta + new_delta,
        updated_at = now()
      where id = new.supply_id;
    else
      update public.grooming_supplies
      set
        current_stock = current_stock - old_delta,
        updated_at = now()
      where id = old.supply_id;

      update public.grooming_supplies
      set
        current_stock = current_stock + new_delta,
        updated_at = now()
      where id = new.supply_id;
    end if;

    return new;
  end if;

  if tg_op = 'DELETE' then
    old_delta := public.get_grooming_supply_stock_delta(
      old.movement_type,
      old.quantity
    );

    update public.grooming_supplies
    set
      current_stock = current_stock - old_delta,
      updated_at = now()
    where id = old.supply_id;

    return old;
  end if;

  return null;
end;
$$;

drop trigger if exists grooming_supply_stock_sync
on public.grooming_supply_movements;

create trigger grooming_supply_stock_sync
after insert or update or delete
on public.grooming_supply_movements
for each row
execute function public.sync_grooming_supply_stock();

