on public.grooming_supplies,
   public.grooming_supply_movements,
   public.groomer_daily_payments
to authenticated;

grant usage, select on all sequences in schema public to authenticated;

commit;

select
  (
    select count(*)
    from information_schema.tables
    where table_schema = 'public'
      and table_name in (
        'grooming_supplies',
        'grooming_supply_movements',
        'groomer_daily_payments'
      )
  ) as grooming_tables,
  (
    select count(*)
    from information_schema.routines
    where specific_schema = 'public'
      and routine_name in (
        'get_grooming_supply_stock_delta',
        'sync_grooming_supply_stock'
      )
  ) as stock_functions,
  (
    select count(*)
    from information_schema.triggers
    where event_object_schema = 'public'
      and event_object_table = 'grooming_supply_movements'
      and trigger_name = 'grooming_supply_stock_sync'
  ) as stock_triggers;
