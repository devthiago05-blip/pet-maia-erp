create table public.pos_sale_returns (
  id bigint generated by default as identity primary key,
  sale_id bigint not null references public.pos_sales(id) on delete restrict,
  return_type text not null check (return_type in ('Devolução','Troca')),
  amount numeric(12,2) not null check (amount > 0),
  reason text not null,
  created_by uuid not null references public.user_profiles(id) on delete restrict,
  created_at timestamptz not null default now()
);
create table public.pos_sale_return_items (
  id bigint generated by default as identity primary key,
  return_id bigint not null references public.pos_sale_returns(id) on delete cascade,
  sale_item_id bigint not null references public.pos_sale_items(id) on delete restrict,
  product_id bigint not null references public.products(id) on delete restrict,
  quantity integer not null check (quantity > 0),
  unit_price numeric(12,2) not null check (unit_price >= 0)
);
create index on public.pos_sale_returns(sale_id,created_at desc);
create index on public.pos_sale_return_items(return_id);
alter table public.pos_sale_returns enable row level security;
alter table public.pos_sale_return_items enable row level security;
create policy pos_returns_read on public.pos_sale_returns for select to authenticated using (public.current_user_can_access('pdv'));
create policy pos_return_items_read on public.pos_sale_return_items for select to authenticated using (public.current_user_can_access('pdv'));
revoke all on public.pos_sale_returns,public.pos_sale_return_items from anon,authenticated;
grant select on public.pos_sale_returns,public.pos_sale_return_items to authenticated;

